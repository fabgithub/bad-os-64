void checksums() {
    char bytes[] = {
        0x45, 0x00, 0x38, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x05, 0x01, 0x00, 0x00, 0xc0, 0xa8, 0x03, 0x02,
        0xc0, 0xa8, 0x03, 0x01
    };

    ASSERT_EQUALS(0xf6a9,
            checksum(bytes, sizeof(bytes), (uint16_t*)(bytes + 8)));
    ASSERT_EQUALS(0xa9f6,
            *(uint16_t*)(bytes + 8));
}

